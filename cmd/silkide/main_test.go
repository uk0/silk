// TestRegenerateGoForSilkui: writes a sibling .silk.go for a saved
// .silkui. Stem-derived function name + standard "package ui" header
// + "Code generated by silkide" provenance line. Exercises the
// in-IDE silkgen pipeline without spawning the cmd/silkgen binary.
//
// Uses decl.Form(..) → ToTDoc → SaveFile to lay down a real .silkui
// fixture; constructing the TDoc text by hand would be brittle to
// formatting changes in core's TDoc serialiser.
func TestRegenerateGoForSilkui(t *testing.T) {
	dir := t.TempDir()
	silkui := filepath.Join(dir, "Editor.silkui")

	// Build a tiny valid decl tree and save it the same way GedScene
	// would. Minimal Form with one Button child — enough to exercise
	// the FromTDoc / ToGo path end-to-end.
	tree := decl.Form(
		decl.ID("Root"),
		decl.P("title", "Editor"),
		decl.Child(decl.Button(decl.ID("ok"), decl.P("text", "OK"))),
	)
	doc := decl.ToTDoc(tree)
	if err := doc.SaveFile(silkui); err != nil {
		t.Fatalf("save fixture: %v", err)
	}

	regenerateGoForSilkui(silkui)

	out := filepath.Join(dir, "Editor.silk.go")
	data, err := os.ReadFile(out)
	if err != nil {
		t.Fatalf("expected %q to exist: %v", out, err)
	}
	for _, want := range []string{
		"// Code generated by silkide — DO NOT EDIT.",
		"package ui",
		`import "silk/decl"`,
		"func BuildEditor() *decl.Node",
	} {
		if !strings.Contains(string(data), want) {
			t.Errorf(".silk.go missing %q\n----\n%s", want, string(data))
		}
	}
}

