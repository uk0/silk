// TestOpenFileInEditorReusesExistingTab: opening the same path
// twice should focus the existing tab rather than stack a duplicate.
// Without dedup, BuildOutput's click-to-jump would multiply tabs
// every time the user clicked an error in the same file.
func TestOpenFileInEditorReusesExistingTab(t *testing.T) {
	// Reset the package map between tests to keep them order-
	// independent.
	openEditors = map[string]*gui.CodeEditor{}

	tabs := gui.NewTabWidget()
	dir := t.TempDir()
	path := filepath.Join(dir, "hello.go")
	if err := os.WriteFile(path, []byte("package main\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	if !openFileInEditor(tabs, path) {
		t.Fatal("first open returned false")
	}
	first := tabs.Count()

	if !openFileInEditor(tabs, path) {
		t.Fatal("second open returned false")
	}
	if tabs.Count() != first {
		t.Errorf("tab count grew on re-open: %d → %d (expected dedup)",
			first, tabs.Count())
	}
}

// TestHasMainPackageDetectsRunnable: a directory with a "package
// main" file is runnable; one without isn't. silkide's Run pre-
// flight uses this to swap the cryptic "package . is not a main
// package" error for a friendly message pointing the user at
// silkide's File→New.
func TestHasMainPackageDetectsRunnable(t *testing.T) {
	dir := t.TempDir()

	// Empty directory → not runnable.
	if hasMainPackage(dir) {
		t.Errorf("empty dir reported runnable")
	}

	// Library-only file → not runnable.
	libPath := filepath.Join(dir, "lib.go")
	if err := os.WriteFile(libPath, []byte("// header comment\n\npackage ui\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if hasMainPackage(dir) {
		t.Errorf("lib-only dir reported runnable")
	}

	// Add a main.go and re-check.
	mainPath := filepath.Join(dir, "main.go")
	if err := os.WriteFile(mainPath, []byte("package main\n\nfunc main() {}\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if !hasMainPackage(dir) {
		t.Errorf("dir with main.go not reported runnable")
	}

	// _test.go files should be ignored by the scan.
	testPath := filepath.Join(dir, "x_test.go")
	if err := os.WriteFile(testPath, []byte("package ui\n\nfunc TestX(t *testing.T) {}\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	if !hasMainPackage(dir) {
		t.Errorf("_test.go shouldn't have flipped runnable=false")
	}
}

// TestFirstPackageLineIsMainSkipsCommentsAndBlanks: the package-
// directive scan must walk past leading // comments and blank
// lines so "// Code generated by …\n\npackage main" still
// resolves to runnable.
func TestFirstPackageLineIsMainSkipsCommentsAndBlanks(t *testing.T) {
	cases := []struct {
		name string
		src  string
		want bool
	}{
		{"plain main", "package main\n", true},
		{"comment then main", "// hello\npackage main\n", true},
		{"blank then main", "\n\npackage main\n", true},
		{"library", "package ui\n", false},
		{"comment then library", "// hi\npackage ui\n", false},
		{"empty", "", false},
		{"just comments", "// foo\n// bar\n", false},
	}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			if got := firstPackageLineIsMain(c.src); got != c.want {
				t.Errorf("got %v, want %v\n%s", got, c.want, c.src)
			}
		})
	}
}

// TestProjectDirResolvesFromActiveScene: a canvas with a scene whose
// filename points at /tmp/work/Editor.silkui resolves to /tmp/work.
// Falling back to os.Getwd happens only when the scene has no path,
// so the resolver behaves like JetBrains' "run in module of the
// active editor" rule.
func TestProjectDirResolvesFromActiveScene(t *testing.T) {
	view := ged.NewGedView()
	scene := view.GedScene()

	// Empty filename → falls back to process cwd.
	got := projectDir(view)
	wantCwd, err := os.Getwd()
	if err != nil {
		t.Fatal(err)
	}
	if got != wantCwd {
		t.Errorf("empty-filename canvas: projectDir = %q, want %q", got, wantCwd)
	}

	scene.SetFilename("/tmp/work/Editor.silkui")
	if got = projectDir(view); got != "/tmp/work" {
		t.Errorf("with filename: projectDir = %q, want %q", got, "/tmp/work")
	}

	if got = projectDir(nil); got != wantCwd {
		t.Errorf("nil canvas: projectDir = %q, want %q (cwd fallback)", got, wantCwd)
	}
}

// TestConfirmDiscardDirtyAllowsCleanScene: a clean scene short-
// circuits the dialog and returns true, so File→New / Open / Quit
// don't pester the user when there's nothing to lose. The dirty
// branch is exercised manually since it requires a modal window.
func TestConfirmDiscardDirtyAllowsCleanScene(t *testing.T) {
	if got := confirmDiscardDirty(nil); !got {
		t.Errorf("nil canvas: got false, want true (no work to lose)")
	}

	view := ged.NewGedView()
	if !confirmDiscardDirty(view) {
		t.Errorf("fresh canvas: got false, want true (no dirty state)")
	}

	// Mark the scene dirty by pushing a real command; the helper
	// must NOT short-circuit through the clean-scene branch when
	// this happens. We can't drive the modal here, so just verify
	// the precondition flips.
	scene := view.GedScene()
	stack := scene.UndoStack()
	if stack == nil {
		t.Fatal("scene has no undo stack")
	}
	if !stack.IsClean() {
		t.Fatal("expected clean stack before mutation")
	}
	scene.SetSize(10, 10)
	// SceneItem.SetSize doesn't push a command on its own — we need
	// to fabricate dirtiness for the precondition check. Use a real
	// MoveCommand-equivalent via a fake widget.
	fake, err := ged.NewFakeWidgetFromFactory("gui.Button")
	if err != nil {
		t.Fatalf("create fake: %v", err)
	}
	fake.SetParent(scene)
	view.Selection().Add(fake)
	cmd := view.Selection().GenerateMoveCommand(5, 5)
	if cmd == nil {
		t.Fatal("expected non-nil MoveCommand")
	}
	stack.Push(cmd)
	if stack.IsClean() {
		t.Errorf("stack should be dirty after Push; helper would short-circuit incorrectly")
	}
}

